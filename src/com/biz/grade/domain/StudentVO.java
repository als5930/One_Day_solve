package com.biz.grade.domain;

public class StudentVO {

	private String num;
	private String name;
	private String dept;
	private int gtade;
	private String tel;
	public String getNum() {
		return num;
	}
	public void setNum(String num) {
		this.num = num;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDept() {
		return dept;
	}
	public void setDept(String dept) {
		this.dept = dept;
	}
	public int getGtade() {
		return gtade;
	}
	public void setGtade(int gtade) {
		this.gtade = gtade;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	
	
	
	
	
	
	
}
